persegi = (int(input("Masukkan panjang sisi: ")))

#Funtion
def hitung_keliling_persegi(sisi):
    hasil = sisi*4
    return hasil
print("Keliling Persegi: %d" % hitung_keliling_persegi(persegi))

def hitung_luas_persegi(sisi):
    hasil = sisi * sisi
    return hasil
print ("Luas Persegi: %d" % hitung_luas_persegi(persegi))

