
persegi = (int(input("Masukkan panjang sisi: ")))

#Prosedur
def hitung_luas_persegi(sisi):
    hasil = sisi * sisi
    print("\nLuas Persegi: %d" % hasil)

hitung_luas_persegi(persegi)

def hitung_keliling_persegi(sisi):
    hasil = sisi * 4
    print("Keliling Persegi: %d" % hasil)

hitung_keliling_persegi(persegi)


